user_month = int(input('Enter the number of month: '))
if user_month in [3, 4, 5]:
    print("It's Spring")
elif user_month in [6, 7, 8]:
    print("It's Summer")
elif user_month in [9, 10, 11]:
    print("It's Autumn")
else:
    print("It's Winter")